void main() {
  // Menggabungkan tiga variabel string
  String kataPertama = "Hello";
  String kataKedua = ",";
  String kataKetiga = " World!";
  
  String hasilGabungan = kataPertama + kataKedua + kataKetiga;
  print(hasilGabungan);

  // Menghitung rumus volume tabung (silinder)
  double jariJari = 5.0;
  double tinggi = 10.0;
  double pi = 3.14159;
  
  double volumeTabung = pi * jariJari * jariJari * tinggi;
  print("Volume tabung dengan jari-jari $jariJari dan tinggi $tinggi adalah $volumeTabung");
}
